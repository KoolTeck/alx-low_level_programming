#include "3-calc.h"
/**
 * op_add - adds two num
 *
 * @a: The ist num
 * @b: The second num
 *
 * Return: the addition value
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subst. two num
 *
 * @a: The ist num
 * @b: The second num
 *
 * Return: the sub value
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two nums
 *
 * @a: The ist num
 * @b: The second num
 *
 * Return: the product's value
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides two nums
 *
 * @a: The ist num
 * @b: The second num
 *
 * Return: the division's value
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - find mod of two num
 *
 * @a: The ist num
 * @b: The second num
 *
 * Return: the remainder of the value
 */
int op_mod(int a, int b)
{
return (a % b);
}
